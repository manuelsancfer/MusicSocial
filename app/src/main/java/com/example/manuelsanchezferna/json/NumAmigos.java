public class NumAmigos {

    private int amigos;

    public int getAmigos() {
        return amigos;
    }

    public void setAmigos(int amigos) {
        this.amigos = amigos;
    }

    @Override
    public String toString() {
        return "NumAmigos{" +
                "amigos=" + amigos +
                '}';
    }
}
